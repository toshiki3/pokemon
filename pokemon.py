class Pokemon:
    def __init__(self, name, level, type, hp, attack, defense, special_attack, special_defense, speed, moves):
        self.name = name
        self.level = level
        self.type = type
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.special_attack = special_attack
        self.special_defense = special_defense
        self.speed = speed
        self.moves = moves